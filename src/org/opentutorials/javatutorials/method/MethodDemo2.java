package org.opentutorials.javatutorials.method;

public class MethodDemo2 {

	public static void main(String[] args) {
		
//		만약 메소드가 없다면?
		int i = 0;
		while(i < 10) {
			System.out.println(i);
			i++;
		}
		
		i = 0;
		while(i < 10) {
			System.out.println(i);
			i++;
		}
		
		i = 0;
		while(i < 10) {
			System.out.println(i);
			i++;
		}
		
//		이것을 1000번 해야 한다면? 각각의 로직이 1000 줄에 육박한다면?
//		메소드를 사용한다면 이러한 문제를 현저히 줄일 수 있다

	}

}
