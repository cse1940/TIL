package org.opentutorials.javatutorials.conditionaloperator;

public class NotDemo {

	public static void main(String[] args) {
		// ! : 부정의 의미로 not이라고 읽음.
		// Boolean의 값을 역전시키는 역할을 한다.
		if (!true) {
            System.out.println(1);
        }
        if (!false) {
            System.out.println(2);
        }

	}

}
