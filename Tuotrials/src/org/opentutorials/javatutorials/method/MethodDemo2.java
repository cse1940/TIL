package org.opentutorials.javatutorials.method;

public class MethodDemo2 {

	public static void main(String[] args) {
		// main : 자바는 main 메소드를 실행하는 관계라고 할 수 있음
		// 메소드가 없는 상태에서,
		// 0부터 9까지를 3번 출력해야 한다면?
		int i=0;
		while(i < 10) {
			System.out.println(i);
			i++;
		}
		i=0;
		while(i < 10) {
			System.out.println(i);
			i++;
		}
		i=0;
		while(i < 10) {
			System.out.println(i);
			i++;
		}

	}

}
