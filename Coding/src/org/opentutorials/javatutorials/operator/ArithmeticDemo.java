package org.opentutorials.javatutorials.operator;

public class ArithmeticDemo {

	public static void main(String[] args) {
		int result = 1 + 2;
		System.out.println(result); // 3
		
		result = result - 1;
		System.out.println(result); // 2
		
		result = result * 2;
		System.out.println(result); // 4
		
		result = result / 2;
		System.out.println(result); // 2
		
		result = result + 8; // 10
		
		result = result % 7;
		System.out.println(result); // 3
				

	}

}
