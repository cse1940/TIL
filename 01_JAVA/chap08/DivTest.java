package chap08;

public class DivTest {
    public static void main(String[] args) {
        double a = 5.0 / 2.0; // 2.5
        System.out.println("a = " + a);

        int b = 4 / 2; // 2
        System.out.println("b = " + b);

        int c = 5 / 2; // 2
        System.out.println("c = " + c);

        double d = 5 / 2; // 2.0
        System.out.println("d = " + d);
    }
}
