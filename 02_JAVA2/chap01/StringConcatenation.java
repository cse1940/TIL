package chap01;

public class StringConcatenation {
    public static void main(String[] args) {
        int month = 12;
        int day = 5;
        int n = 2;
        double weight = 60.0;

        System.out.println("=========================");
        System.out.println("승은이의 " + month + "월 " + day + "일 다이어트 일지");
        System.out.println("=========================");
        System.out.println("식사 횟수: " + n + "회");
        System.out.println("몸무게: " + weight + "kg");
    }
}